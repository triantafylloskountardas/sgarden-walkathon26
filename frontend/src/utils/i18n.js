const I18nContext = createContext({
	language: "en",
	setLanguage: () => {},
	t: (key) => key,
});

const formatMessage = (message, params = {}) => message.replaceAll(/\{(\w+)\}/g, (_, token) => params[token] ?? `{${token}}`);

export const I18nProvider = ({ children }) => {
	const [language, setLanguage] = useState(() => {
		if (typeof window === "undefined") return "en";
		return window.localStorage.getItem(LANGUAGE_STORAGE_KEY) || "en";
	});

	useEffect(() => {
		if (typeof window !== "undefined") {
			window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
		}

		document.documentElement.lang = language;
		dayjs.locale(language === "el" ? "el" : "en");
	}, [language]);

	const value = useMemo(() => ({
		language,
		setLanguage,
		t: (key, params = {}) => {
			const dictionary = dictionaries[language] || dictionaries.en;
			const message = dictionary[key] || dictionaries.en[key] || key;
			return typeof message === "string" ? formatMessage(message, params) : key;
		},
	}), [language]);

	return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);

export default useI18n;
